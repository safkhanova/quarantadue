#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (src > dest)
	{
		while (i < n)
		{
			*(unsigned char *)dest++ = *(unsigned char *)src++;
			i++;
		}
		
	}
	return dest;

}
